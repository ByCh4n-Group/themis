randomexitmessage() {
    case $(( ${RANDOM} % 3 )) in
        0)
              set_randomexitmessage="Topluluk Sunucumuzda Bizimle Birlikte Sohbet Edebilir Yeni Projeleri Geliştirmemizde Bize Yardımcı Olabilirsiniz: https://discord.com/invite/dQbvahVVG4"
        ;;
        1)
            set_randomexitmessage="GNU/Linux PNM OS Projesini seveceksiniz: https://pnmos.xyz"
        ;;
        2)
            set_randomexitmessage="Eskiden ByCh4n ekibinde kaliteli insanlar varken (yani en azından biz öyle sanarken) güzel projelerimiz vardı bunların bir kısmını halen https://github.com/ByCh4n-Group adresinde geliştirmeye devam ediyoruz"
